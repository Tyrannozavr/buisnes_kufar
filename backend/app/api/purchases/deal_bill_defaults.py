"""Начальные значения полей счёта при создании сделки.

Строки — сроки в днях для подстановки в шаблоны; пользователь может изменить или очистить.

Тексты условий совпадают с frontend/constants/contractTerms.ts:
CONTRACT_TERMS_BILL_CONTRACT.DELIVERY_SUPPLIER_WITH_PAYMENT_AND_DELIVERY,
CONTRACT_TERMS_BILL_OFFER.DELIVERY_SUPPLIER_PAYMENT.
"""

DEFAULT_BILL_PAYMENT_TERMS_DAYS = "3"
DEFAULT_BILL_DELIVERY_TERMS_DAYS = "10"


def default_contract_terms_text_contract(
	number: str = "",
	date: str = "",
	payment_terms_contract: str = DEFAULT_BILL_PAYMENT_TERMS_DAYS,
	delivery_terms_contract: str = DEFAULT_BILL_DELIVERY_TERMS_DAYS,
) -> str:
	"""Как CONTRACT_TERMS_BILL_CONTRACT.DELIVERY_SUPPLIER_WITH_PAYMENT_AND_DELIVERY на фронте."""
	return (
		f"Основные условия настоящего договора-счета № {number} от {date} г.\n"
		'1. \tПредметом настоящего Счета-договора является поставка товарно-материальных ценностей (далее - "товар").\n'
		"2. \tОплата настоящего Счета-договора означает согласие Покупателя с условиями оплаты и поставки товара.\t\n"
		f"3. \tНастоящий Счет-договор действителен в течение {payment_terms_contract} рабочих дней от даты его составления включительно. При отсутствии оплаты в указанный срок настоящий Счет-договор признается недействительным.\n"
		f"4. \tПоставщик обязан доставить оплаченный товар и передать его Покупателю в течение {delivery_terms_contract} рабочих дней с момента зачисления оплаты на расчетный счет\n"
		"5. \tОплаченный товар доставляется Покупателю силами ПОСТАВЩИКА\n"
		"6. \tОплата Счета-договора третьими лицами (сторонами), а также неполная (частичная) оплата Счета-договора не допускается. Покупатель не имеет права производить выборочную оплату позиций счета и требовать поставку товара по выбранным позициям.\n"
		"7. \tПоставщик вправе не выполнять поставку товара до зачисления оплаты на расчетный счет.\n"
		"8. \tПокупатель обязан принять оплаченный товар лично или через уполномоченного представителя. Передача товара осуществляется при предъявлении документа, удостоверяющего личность, и/или доверенности оформленной в установленном порядке.\n"
		"9. \tПодписание Покупателем или его уполномоченным представителем товарной накладной означает согласие Покупателя с комплектностью и надлежащим качеством товара."
	)


def default_contract_terms_text_offer(
	payment_terms_offer: str = DEFAULT_BILL_PAYMENT_TERMS_DAYS,
	production_address: str = "",
) -> str:
	"""Как CONTRACT_TERMS_BILL_OFFER.DELIVERY_SUPPLIER_PAYMENT на фронте."""
	addr = production_address if production_address else ""
	return (
		"1.\tПредметом настоящего счета-оферты является поставка товара по перечню изделий поставщиком покупателю.\n"
		"2.\tПодписывая настоящий счет-оферту, Покупатель дает согласие на то, что товар надлежащего качества обмену и возврату не подлежит.\n"
		"3.\tОсмотр товара Покупателем происходит при получении. Покупатель проводит обследование единиц продукции на предмет отсутствия брака и дефектов, проверяет комплектность партии. При обнаружении недочетов Покупателем составляется акт. При отсутствии акта Поставщик претензии не принимает.\n"
		f"4.\tПокупатель обязуется оплатить товар на условиях 100% предоплаты в сумме, указанной в счете, в течение {payment_terms_offer} рабочих дней по указанным реквизитам.\n"
		f"5.\tОплаченный товар доставляется Покупателю силами ПОСТАВЩИКА со склада Поставщика, расположенного по адресу: {addr}.\n"
		"6.\tПосле получения товара Покупатель обязан подписать Товарную накладную."
	)
